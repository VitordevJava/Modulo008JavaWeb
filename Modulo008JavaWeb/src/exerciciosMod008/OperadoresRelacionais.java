package exerciciosMod008;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		int nota1= 93;
		int nota2= 90;
	
		/* Para serem comparados se são iguais serão usados dois sinais de igualdade : == /
		 *  já se são diferentes será usado a exclamação de junto do sinal da igualdade : != /
		 */
		
		if (nota1 != nota2) {
			System.out.println("As notas são diferentes");
		}else {
			System.out.println("As notas são iguais");			
		}
	}
}
